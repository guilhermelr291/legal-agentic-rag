"""Database repository layer for document ingestion.

Provides type-safe repository classes for documents and chunks with
RLS-aware operations via SupabaseClient.
"""

from __future__ import annotations

import logging
from datetime import datetime
from typing import Any, Literal

from pydantic import BaseModel, Field

from services.storage.supabase_client import DatabaseError, SupabaseClient

logger = logging.getLogger(__name__)


# =============================================================================
# Pydantic Models for Type Safety
# =============================================================================


class DocumentCreate(BaseModel):
    """Input model for creating a new document record."""

    user_id: str = Field(..., description="RLS user identifier")
    filename: str = Field(..., description="Original filename")
    file_type: Literal["pdf", "docx", "xlsx"] = Field(
        ..., description="Document type"
    )
    file_size: int = Field(..., ge=0, description="File size in bytes")
    storage_path: str = Field(
        ..., description="Path in Supabase Storage: {user_id}/{document_id}/{filename}"
    )
    status: Literal["processing", "ready", "failed"] = Field(
        default="processing", description="Current processing status"
    )
    error_msg: str | None = Field(default=None, description="Error message if failed")
    meta: dict[str, Any] = Field(
        default_factory=dict, description="JSONB metadata (XLSX info, graph status)"
    )


class DocumentRecord(BaseModel):
    """Complete document record as stored in the database."""

    id: str = Field(..., description="UUID primary key")
    user_id: str = Field(..., description="RLS user identifier")
    filename: str = Field(..., description="Original filename")
    file_type: Literal["pdf", "docx", "xlsx"] = Field(..., description="Document type")
    file_size: int = Field(..., ge=0, description="File size in bytes")
    storage_path: str = Field(..., description="Path in Supabase Storage")
    status: Literal["processing", "ready", "failed"] = Field(
        ..., description="Current processing status"
    )
    error_msg: str | None = Field(
        default=None, description="Error message if processing failed"
    )
    meta: dict[str, Any] = Field(
        default_factory=dict, description="JSONB metadata"
    )
    created_at: datetime = Field(..., description="Record creation timestamp")
    updated_at: datetime = Field(..., description="Last update timestamp")

    @classmethod
    def from_db_row(cls, row: dict[str, Any]) -> DocumentRecord:
        """Create a DocumentRecord from a database row dictionary.

        Handles datetime parsing from ISO format strings.
        """
        data = dict(row)

    
        for field in ["created_at", "updated_at"]:
            if field in data and isinstance(data[field], str):
                data[field] = datetime.fromisoformat(
                    data[field].replace("Z", "+00:00")
                )

        return cls(**data)


class ChunkRecord(BaseModel):
    """Chunk record as stored in the database with embeddings."""

    id: str | None = Field(
        default=None, description="UUID primary key (auto-generated by DB)"
    )
    document_id: str = Field(..., description="FK to documents table")
    user_id: str = Field(..., description="RLS user identifier")
    chunk_index: int = Field(..., ge=0, description="Sequential index within document")
    content: str = Field(..., description="Chunk text content")
    embedding: list[float] | None = Field(
        default=None, description="1536-dim OpenAI embedding vector"
    )
    section_hint: str | None = Field(
        default=None, description="Detected heading (e.g., 'Clause 12', 'Art. 5')"
    )
    section_path: list[str] = Field(
        default_factory=list, description="Hierarchy breadcrumbs"
    )
    page_start: int | None = Field(default=None, description="Starting page number")
    page_end: int | None = Field(default=None, description="Ending page number")
    anchors: list[str] = Field(
        default_factory=list, description="Detected references in text"
    )
    char_start: int = Field(..., ge=0, description="Start character offset")
    char_end: int = Field(..., ge=0, description="End character offset")
    created_at: datetime | None = Field(
        default=None, description="Record creation timestamp"
    )

    def to_db_dict(self) -> dict[str, Any]:
        """Convert to dictionary for database insertion.

        Excludes auto-generated fields (id, created_at) and handles
        the embedding vector format for pgvector.
        """
        data = self.model_dump(
            exclude={"id", "created_at"},
            exclude_none=False,
        )

   
        data["section_path"] = list(self.section_path) if self.section_path else []
        data["anchors"] = list(self.anchors) if self.anchors else []

      
        if self.embedding is not None:
            data["embedding"] = self.embedding
        else:
            data.pop("embedding", None)

        return data

    @classmethod
    def from_db_row(cls, row: dict[str, Any]) -> ChunkRecord:
        """Create a ChunkRecord from a database row dictionary."""
        data = dict(row)

       
        if "created_at" in data and isinstance(data["created_at"], str):
            data["created_at"] = datetime.fromisoformat(
                data["created_at"].replace("Z", "+00:00")
            )

     
        for field in ["section_path", "anchors"]:
            if field in data and data[field] is None:
                data[field] = []

        return cls(**data)


